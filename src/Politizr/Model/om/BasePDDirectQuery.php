/**
 * @method PDDirectQuery orderById($order = Criteria::ASC) Order by the id column
 * @method PDDirectQuery orderByName($order = Criteria::ASC) Order by the name column
 * @method PDDirectQuery orderByCity($order = Criteria::ASC) Order by the city column
 * @method PDDirectQuery orderByEmail($order = Criteria::ASC) Order by the email column
 * @method PDDirectQuery orderByDescription($order = Criteria::ASC) Order by the description column
 * @method PDDirectQuery orderByCreatedAt($order = Criteria::ASC) Order by the created_at column
 * @method PDDirectQuery orderByUpdatedAt($order = Criteria::ASC) Order by the updated_at column
 *
 * @method PDDirectQuery groupById() Group by the id column
 * @method PDDirectQuery groupByName() Group by the name column
 * @method PDDirectQuery groupByCity() Group by the city column
 * @method PDDirectQuery groupByEmail() Group by the email column
 * @method PDDirectQuery groupByDescription() Group by the description column
 * @method PDDirectQuery groupByCreatedAt() Group by the created_at column
 * @method PDDirectQuery groupByUpdatedAt() Group by the updated_at column
 *
 * @method PDDirectQuery leftJoin($relation) Adds a LEFT JOIN clause to the query
 * @method PDDirectQuery rightJoin($relation) Adds a RIGHT JOIN clause to the query
 * @method PDDirectQuery innerJoin($relation) Adds a INNER JOIN clause to the query
 *
 * @method PDDirect findOne(PropelPDO $con = null) Return the first PDDirect matching the query
 * @method PDDirect findOneOrCreate(PropelPDO $con = null) Return the first PDDirect matching the query, or a new PDDirect object populated from the query conditions when no match is found
 *
 * @method PDDirect findOneByName(string $name) Return the first PDDirect filtered by the name column
 * @method PDDirect findOneByCity(string $city) Return the first PDDirect filtered by the city column
 * @method PDDirect findOneByEmail(string $email) Return the first PDDirect filtered by the email column
 * @method PDDirect findOneByDescription(string $description) Return the first PDDirect filtered by the description column
 * @method PDDirect findOneByCreatedAt(string $created_at) Return the first PDDirect filtered by the created_at column
 * @method PDDirect findOneByUpdatedAt(string $updated_at) Return the first PDDirect filtered by the updated_at column
 *
 * @method array findById(int $id) Return PDDirect objects filtered by the id column
 * @method array findByName(string $name) Return PDDirect objects filtered by the name column
 * @method array findByCity(string $city) Return PDDirect objects filtered by the city column
 * @method array findByEmail(string $email) Return PDDirect objects filtered by the email column
 * @method array findByDescription(string $description) Return PDDirect objects filtered by the description column
 * @method array findByCreatedAt(string $created_at) Return PDDirect objects filtered by the created_at column
 * @method array findByUpdatedAt(string $updated_at) Return PDDirect objects filtered by the updated_at column
 */
abstract class BasePDDirectQuery extends ModelCriteria
{
    // archivable behavior
    protected $archiveOnDelete = true;

    /**
     * Initializes internal state of BasePDDirectQuery object.
     *
     * @param     string $dbName The dabase name
     * @param     string $modelName The phpName of a model, e.g. 'Book'
     * @param     string $modelAlias The alias for the model in this query, e.g. 'b'
     */
    public function __construct($dbName = null, $modelName = null, $modelAlias = null)
    {
        if (null === $dbName) {
            $dbName = 'default';
        }
        if (null === $modelName) {
            $modelName = 'Politizr\\Model\\PDDirect';
        }
        parent::__construct($dbName, $modelName, $modelAlias);
    }

    /**
     * Returns a new PDDirectQuery object.
     *
     * @param     string $modelAlias The alias of a model in the query
     * @param   PDDirectQuery|Criteria $criteria Optional Criteria to build the query from
     *
     * @return PDDirectQuery
     */
    public static function create($modelAlias = null, $criteria = null)
    {
        if ($criteria instanceof PDDirectQuery) {
            return $criteria;
        }
        $query = new PDDirectQuery(null, null, $modelAlias);

        if ($criteria instanceof Criteria) {
            $query->mergeWith($criteria);
        }

        return $query;
    }

    /**
     * Find object by primary key.
     * Propel uses the instance pool to skip the database if the object exists.
     * Go fast if the query is untouched.
     *
     * <code>
     * $obj  = $c->findPk(12, $con);
     * </code>
     *
     * @param mixed $key Primary key to use for the query
     * @param     PropelPDO $con an optional connection object
     *
     * @return   PDDirect|PDDirect[]|mixed the result, formatted by the current formatter
     */
    public function findPk($key, $con = null)
    {
        if ($key === null) {
            return null;
        }
        if ((null !== ($obj = PDDirectPeer::getInstanceFromPool((string) $key))) && !$this->formatter) {
            // the object is already in the instance pool
            return $obj;
        }
        if ($con === null) {
            $con = Propel::getConnection(PDDirectPeer::DATABASE_NAME, Propel::CONNECTION_READ);
        }
        $this->basePreSelect($con);
        if ($this->formatter || $this->modelAlias || $this->with || $this->select
         || $this->selectColumns || $this->asColumns || $this->selectModifiers
         || $this->map || $this->having || $this->joins) {
            return $this->findPkComplex($key, $con);
        } else {
            return $this->findPkSimple($key, $con);
        }
    }

    /**
     * Alias of findPk to use instance pooling
     *
     * @param     mixed $key Primary key to use for the query
     * @param     PropelPDO $con A connection object
     *
     * @return                 PDDirect A model object, or null if the key is not found
     * @throws PropelException
     */
     public function findOneById($key, $con = null)
     {
        return $this->findPk($key, $con);
     }

    /**
     * Find object by primary key using raw SQL to go fast.
     * Bypass doSelect() and the object formatter by using generated code.
     *
     * @param     mixed $key Primary key to use for the query
     * @param     PropelPDO $con A connection object
     *
     * @return                 PDDirect A model object, or null if the key is not found
     * @throws PropelException
     */
    protected function findPkSimple($key, $con)
    {
        $sql = 'SELECT `id`, `name`, `city`, `email`, `description`, `created_at`, `updated_at` FROM `p_d_direct` WHERE `id` = :p0';
        try {
            $stmt = $con->prepare($sql);
            $stmt->bindValue(':p0', $key, PDO::PARAM_INT);
            $stmt->execute();
        } catch (Exception $e) {
            Propel::log($e->getMessage(), Propel::LOG_ERR);
            throw new PropelException(sprintf('Unable to execute SELECT statement [%s]', $sql), $e);
        }
        $obj = null;
        if ($row = $stmt->fetch(PDO::FETCH_NUM)) {
            $obj = new PDDirect();
            $obj->hydrate($row);
            PDDirectPeer::addInstanceToPool($obj, (string) $key);
        }
        $stmt->closeCursor();

        return $obj;
    }

    /**
     * Find object by primary key.
     *
     * @param     mixed $key Primary key to use for the query
     * @param     PropelPDO $con A connection object
     *
     * @return PDDirect|PDDirect[]|mixed the result, formatted by the current formatter
     */
    protected function findPkComplex($key, $con)
    {
        // As the query uses a PK condition, no limit(1) is necessary.
        $criteria = $this->isKeepQuery() ? clone $this : $this;
        $stmt = $criteria
            ->filterByPrimaryKey($key)
            ->doSelect($con);

        return $criteria->getFormatter()->init($criteria)->formatOne($stmt);
    }

    /**
     * Find objects by primary key
     * <code>
     * $objs = $c->findPks(array(12, 56, 832), $con);
     * </code>
     * @param     array $keys Primary keys to use for the query
     * @param     PropelPDO $con an optional connection object
     *
     * @return PropelObjectCollection|PDDirect[]|mixed the list of results, formatted by the current formatter
     */
    public function findPks($keys, $con = null)
    {
        if ($con === null) {
            $con = Propel::getConnection($this->getDbName(), Propel::CONNECTION_READ);
        }
        $this->basePreSelect($con);
        $criteria = $this->isKeepQuery() ? clone $this : $this;
        $stmt = $criteria
            ->filterByPrimaryKeys($keys)
            ->doSelect($con);

        return $criteria->getFormatter()->init($criteria)->format($stmt);
    }

    /**
     * Filter the query by primary key
     *
     * @param     mixed $key Primary key to use for the query
     *
     * @return PDDirectQuery The current query, for fluid interface
     */
    public function filterByPrimaryKey($key)
    {

        return $this->addUsingAlias(PDDirectPeer::ID, $key, Criteria::EQUAL);
    }

    /**
     * Filter the query by a list of primary keys
     *
     * @param     array $keys The list of primary key to use for the query
     *
     * @return PDDirectQuery The current query, for fluid interface
     */
    public function filterByPrimaryKeys($keys)
    {

        return $this->addUsingAlias(PDDirectPeer::ID, $keys, Criteria::IN);
    }

    /**
     * Filter the query on the id column
     *
     * Example usage:
     * <code>
     * $query->filterById(1234); // WHERE id = 1234
     * $query->filterById(array(12, 34)); // WHERE id IN (12, 34)
     * $query->filterById(array('min' => 12)); // WHERE id >= 12
     * $query->filterById(array('max' => 12)); // WHERE id <= 12
     * </code>
     *
     * @param     mixed $id The value to use as filter.
     *              Use scalar values for equality.
     *              Use array values for in_array() equivalent.
     *              Use associative array('min' => $minValue, 'max' => $maxValue) for intervals.
     * @param     string $comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return PDDirectQuery The current query, for fluid interface
     */
    public function filterById($id = null, $comparison = null)
    {
        if (is_array($id)) {
            $useMinMax = false;
            if (isset($id['min'])) {
                $this->addUsingAlias(PDDirectPeer::ID, $id['min'], Criteria::GREATER_EQUAL);
                $useMinMax = true;
            }
            if (isset($id['max'])) {
                $this->addUsingAlias(PDDirectPeer::ID, $id['max'], Criteria::LESS_EQUAL);
                $useMinMax = true;
            }
            if ($useMinMax) {
                return $this;
            }
            if (null === $comparison) {
                $comparison = Criteria::IN;
            }
        }

        return $this->addUsingAlias(PDDirectPeer::ID, $id, $comparison);
    }

    /**
     * Filter the query on the name column
     *
     * Example usage:
     * <code>
     * $query->filterByName('fooValue');   // WHERE name = 'fooValue'
     * $query->filterByName('%fooValue%'); // WHERE name LIKE '%fooValue%'
     * </code>
     *
     * @param     string $name The value to use as filter.
     *              Accepts wildcards (* and % trigger a LIKE)
     * @param     string $comparison Operator to use for the column comparison, defaults to Criteria::EQUAL
     *
     * @return PDDirectQuery The current query, for fluid interface
     */
    public function filterByName($name = null, $comparison = null)
    {
        if (null === $comparison) {
            if (is_array($name)) {
                $comparison = Criteria::IN;
            } elseif (preg_match('/[\%\*]/', $name)) {
                $name = str_replace('*', '%', $name);
                $comparison = Criteria::LIKE;
            }
        }

        return $this->addUsingAlias(PDDirectPeer::NAME, $name, $comparison);
    }

