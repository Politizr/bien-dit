     * @param   PDDirect $pDDirect Object to remove from the list of results
     *
     * @return PDDirectQuery The current query, for fluid interface
     */
    public function prune($pDDirect = null)
    {
        if ($pDDirect) {
            $this->addUsingAlias(PDDirectPeer::ID, $pDDirect->getId(), Criteria::NOT_EQUAL);
        }

        return $this;
    }

    /**
     * Code to execute before every DELETE statement
     *
     * @param     PropelPDO $con The connection object used by the query
     */
    protected function basePreDelete(PropelPDO $con)
    {
        // archivable behavior

        if ($this->archiveOnDelete) {
            $this->archive($con);
        } else {
            $this->archiveOnDelete = true;
        }


        return $this->preDelete($con);
    }

    // timestampable behavior

    /**
