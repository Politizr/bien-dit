 * @method PMCharte findOne(PropelPDO $con = null) Return the first PMCharte matching the query
 * @method PMCharte findOneOrCreate(PropelPDO $con = null) Return the first PMCharte matching the query, or a new PMCharte object populated from the query conditions when no match is found
