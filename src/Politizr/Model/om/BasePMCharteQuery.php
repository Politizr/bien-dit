    /**
     * Exclude object from result
     *
     * @param   PMCharte $pMCharte Object to remove from the list of results
     *
     * @return PMCharteQuery The current query, for fluid interface
     */
    public function prune($pMCharte = null)
    {
        if ($pMCharte) {
            $this->addUsingAlias(PMChartePeer::ID, $pMCharte->getId(), Criteria::NOT_EQUAL);
        }

        return $this;
    }

    // timestampable behavior

    /**
     * Filter by the latest updated
     *
     * @param      int $nbDays Maximum age of the latest update in days
     *
     * @return     PMCharteQuery The current query, for fluid interface
     */
    public function recentlyUpdated($nbDays = 7)
    {
        return $this->addUsingAlias(PMChartePeer::UPDATED_AT, time() - $nbDays * 24 * 60 * 60, Criteria::GREATER_EQUAL);
    }

